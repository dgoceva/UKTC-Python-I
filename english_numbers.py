digit = int(input('Input digit [0-9]: '))

if digit == 0:
    print('{0} => {1}'.format(digit, 'zero'))
elif digit == 1:
    print('{0} => {1}'.format(digit, 'one'))
elif digit == 2:
    print('{0} => {1}'.format(digit, 'two'))
elif digit == 3:
    print('{0} => {1}'.format(digit, 'three'))
elif digit == 4:
    print('{0} => {1}'.format(digit, 'four'))
elif digit == 5:
    print('{0} => {1}'.format(digit, 'five'))
elif digit == 6:
    print('{0} => {1}'.format(digit, 'six'))
elif digit == 7:
    print('{0} => {1}'.format(digit, 'seven'))
elif digit == 8:
    print('{0} => {1}'.format(digit, 'eight'))
elif digit == 9:
    print('{0} => {1}'.format(digit, 'nine'))
else:
    print('Wrong number')
